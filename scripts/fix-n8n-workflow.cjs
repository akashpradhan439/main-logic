const jsCode = [
  '// === GENERATE + SUPERVISOR LOOP ===',
  '// Each connection carries: connectionId (stable), firstName (display), suggestionType, signals.',
  '// Detailed: { type, connectionId, connectionName, title, place, time, text }',
  '// One-liner: { type, connectionId, connectionName, text }',
  '// Identity = connectionId. firstName is display-only and may collide (e.g. two "Yash").',
  '',
  'const input = $input.first().json;',
  'const context = input.context;',
  'const places = Array.isArray(input.places) ? input.places : [];',
  'let connections = Array.isArray(context.connections) ? context.connections : [];',
  "const languageLabel = (typeof context.languageLabel === 'string' && context.languageLabel.trim()) ? context.languageLabel.trim() : 'English';",
  "const languageInstruction = languageLabel === 'English'",
  "  ? 'All user-facing text fields (title, place, time, text) MUST be written in English.'",
  "  : ('All user-facing text fields (title, time, text) MUST be written in ' + languageLabel + '. JSON keys, type values (\"detailed\"/\"one_liner\"), connectionId, and the place name (which is a real-world location label) MUST stay as-is. connectionName MUST stay exactly as provided (do not transliterate).');",
  '',
  '// Edge: no connections → return empty, no Groq calls.',
  '// Edge: no nearby places → force everyone to one_liner (detailed needs a real place).',
  'if (connections.length === 0) {',
  "  return [{ json: { suggestions: [], attempts: 0, approved: true, lastFeedback: 'no_connections' } }];",
  '}',
  'if (places.length === 0) {',
  "  connections = connections.map(c => ({ ...c, suggestionType: 'one_liner' }));",
  '}',
  '',
  '// LLM backend: Azure AI Foundry (OpenAI-compatible). Endpoint already ends in /openai/v1.',
  "const GROQ_URL = ($env.AZURE_OPENAI_ENDPOINT || '').replace(/\\/$/, '') + '/chat/completions';",
  'const GROQ_KEY = $env.AZURE_OPENAI_API_KEY;',
  "const MODEL = $env.AZURE_OPENAI_DEPLOYMENT || 'Llama-3.3-70B-Instruct';",
  'const MAX_ATTEMPTS = 4;',
  '',
  '// Banned phrases — block only unambiguous marketing-speak and AI-tells where',
  "// the assistant claims to perform tasks it can't. Natural turns of phrase",
  "// ('great spot', 'grab coffee', 'perfect for her', 'how about a walk') are",
  '// tolerated — the prompt still discourages clichés, but the deterministic gate',
  '// stays narrow so the supervisor can approve without burning every attempt.',
  'const BANNED_PATTERNS = [',
  "  /\\bperfect (place|spot)\\b/i,",
  "  /\\bsounds? (great|perfect|fun)\\b/i,",
  "  /\\blet me (draft|plan|arrange)\\b/i,",
  "  /\\b(I'?ll|I will) (plan|draft|set up|arrange)\\b/i,",
  '];',
  '',
  'async function groq(messages, temperature, maxTokens) {',
  '  return await this.helpers.httpRequest({',
  '    method: \'POST\', url: GROQ_URL,',
  "    headers: { Authorization: 'Bearer ' + GROQ_KEY, 'Content-Type': 'application/json' },",
  "    body: { model: MODEL, temperature, max_tokens: maxTokens, response_format: { type: 'json_object' }, messages },",
  '    json: true,',
  '  });',
  '}',
  '',
  'const GENERATION_SYSTEM = [',
  '  "You are the in-app planner that writes personalised meet-up cards for a privacy-first social app.",',
  '  "Voice: warm, specific, like a thoughtful friend who knows both people. Never marketing-speak. Never hedge.",',
  '  languageInstruction,',
  '  "",',
  '  "INPUT: a user + a list of their connections. Each connection has:",',
  '  "  connectionId (stable identifier — use this for identity)",',
  '  "  firstName (display name — may collide with other connections)",',
  '  "  bio, interests, sharedInterests (overlap with user), nearby (true/false)",',
  '  "  suggestionType: \\"detailed\\" or \\"one_liner\\" — you MUST match this per connection.",',
  '  "",',
  '  "OUTPUT one object per connection in {\\"suggestions\\":[...]}, matching suggestionType:",',
  '  "",',
  '  "DETAILED ({\\"type\\":\\"detailed\\", \\"connectionId\\", \\"connectionName\\", \\"title\\", \\"place\\", \\"time\\", \\"text\\"})",',
  '  "  title: 3-5 words, names the activity + person, references a real detail (\\"Sunset photo walk with Aditya\\", not \\"Walk with Aditya\\")",',
  '  "  place: EXACT name from the provided nearby spots list (character-for-character)",',
  '  "  time: human-readable like \\"Tonight, 7:00 PM\\" / \\"Tomorrow, 6:30 PM\\" / \\"Saturday, 10:00 AM\\". Choose activity-appropriate (dinner→evening, coffee→morning, walk→golden hour). Anchor to today\'s date.",',
  '  "  text: ONE sentence, 60-100 chars. Concrete hook tying the shared interest to the place or time. Must include connectionName. No question — it\'s already a plan.",',
  '  "",',
  '  "ONE_LINER ({\\"type\\":\\"one_liner\\", \\"connectionId\\", \\"connectionName\\", \\"text\\"})",',
  '  "  text: ONE sentence, 40-110 chars. Names a specific shared detail (interest, recent context, mutual taste) then ends with a soft invitation as a question. Must include connectionName.",',
  '  "",',
  '  "HARD RULES — failures will be rejected mechanically:",',
  '  "  • Use connectionId verbatim from input. Use the firstName from input for connectionName.",',
  '  "  • For detailed: place MUST be an exact name from nearby spots. No invented places.",',
  '  "  • text MUST contain connectionName.",',
  '  "  • Exactly ONE suggestion per connection — no duplicates, no skips.",',
  '  "  • Type MUST match the suggestionType assigned to that connection.",',
  '  "  • Vary opening words across the set (don\'t start every text with the name + comma).",',
  '  "",',
  '  "BANNED phrasing (will be auto-rejected): \\"great atmosphere\\", \\"perfect spot\\", \\"sounds great\\", \\"fancy a\\", \\"hang out\\", \\"grab a coffee\\", \\"let me draft\\", \\"I\'ll plan\\", \\"would love to\\".",',
  '  "",',
  '  "GOOD detailed examples (notice the specificity):",',
  '  "  {\\"type\\":\\"detailed\\",\\"connectionId\\":\\"u_1\\",\\"connectionName\\":\\"Sam\\",\\"title\\":\\"Jazz dinner with Sam\\",\\"place\\":\\"Blue Tokai Coffee\\",\\"time\\":\\"Tonight, 7:00 PM\\",\\"text\\":\\"Sam\'s into live jazz — Blue Tokai has a Friday set you\'d both love.\\"}",',
  '  "  {\\"type\\":\\"detailed\\",\\"connectionId\\":\\"u_2\\",\\"connectionName\\":\\"Maya\\",\\"title\\":\\"Pour-over with Maya\\",\\"place\\":\\"Cafe Loup\\",\\"time\\":\\"Tomorrow, 10:00 AM\\",\\"text\\":\\"Cafe Loup\'s slow pour matches Maya\'s thing for specialty roasts.\\"}",',
  '  "",',
  '  "GOOD one_liner examples (notice the specific shared detail):",',
  '  "  {\\"type\\":\\"one_liner\\",\\"connectionId\\":\\"u_3\\",\\"connectionName\\":\\"Priya\\",\\"text\\":\\"You and Priya both starred Lisbon — want me to draft a 3-day plan?\\"}",',
  '  "  {\\"type\\":\\"one_liner\\",\\"connectionId\\":\\"u_4\\",\\"connectionName\\":\\"Ram\\",\\"text\\":\\"Ram\'s back from his Himalayan trek — catch up over a long walk this week?\\"}",',
  '  "",',
  '  "BAD examples (do NOT output anything like these):",',
  '  "  {\\"text\\":\\"Coffee with Maya at Cafe Loup, sounds great!\\"}  // banned phrase",',
  '  "  {\\"text\\":\\"Aditya and you share a love for photography — Khanpur Shiv Park has great spots.\\"}  // banned + vague",',
  '  "  {\\"title\\":\\"Walk with Aditya\\"}  // no specific detail",',
  '].join("\\n");',
  '',
  'const SUPERVISOR_SYSTEM = [',
  '  "You are a friendly editor doing a final sanity pass on meet-up suggestion cards before they reach the user.",',
  '  ("All user-facing text must read naturally in " + languageLabel + ". Reject items where the language is wrong, mangled, or only partially translated."),',
  '  "Default to APPROVED. Only REJECT when a suggestion has a clear, serious problem that would embarrass the app. Minor imperfections are fine — do not nitpick.",',
  '  "Serious problems worth rejecting:",',
  '  "",',
  '  "1. EMPTY/GENERIC — says nothing concrete at all (pure filler with no interest, place, or reason). A light shared detail is acceptable; only reject truly contentless text.",',
  '  "2. VOICE — reads like an ad or a robot (marketing-speak, exclamation spam). Ordinary friendly phrasing is fine.",',
  '  "3. BROKEN INTENT — clearly the wrong shape (e.g. a one_liner that is plainly not an invitation).",',
  '  "",',
  '  "Type/identity/place accuracy, length, question-mark format, and variety are already mechanically verified — do NOT re-check those.",',
  '  "",',
  '  \'Return ONLY: {"verdict":"APPROVED","feedback":""} or {"verdict":"REJECTED","feedback":"per-item: connectionId X — rule N — quote — fix"}\'',
  '].join("\\n");',
  '',
  'const placesForPrompt = places.map(p => ({ name: p.name, address: p.address, distance_m: p.distance_meters }));',
  'const connectionsForPrompt = connections.map(c => ({',
  '  connectionId: c.userId,',
  '  firstName: c.firstName,',
  '  bio: c.bio,',
  '  interests: c.interests,',
  '  sharedInterests: c.signals ? c.signals.sharedInterests : [],',
  '  nearby: c.signals ? c.signals.nearby : false,',
  '  suggestionType: c.suggestionType,',
  '}));',
  '',
  '// Expected map: connectionId -> { firstName, suggestionType }',
  'const expectedById = new Map();',
  'for (const c of connectionsForPrompt) expectedById.set(c.connectionId, { firstName: c.firstName, type: c.suggestionType });',
  '',
  'let lastSuggestions = [];',
  "let feedback = '';",
  'let approved = false;',
  'let completedAttempts = 0;',
  '',
  'for (let attempt = 1; attempt <= MAX_ATTEMPTS; attempt++) {',
  '  completedAttempts = attempt;',
  "  const feedbackBlock = feedback ? ('\\n\\nPREVIOUS ATTEMPT REJECTED. Address every item below:\\n' + feedback) : '';",
  "  const genUser = 'USER\\n  firstName: ' + context.firstName + '\\n  bio: ' + (context.bio || '(none)') + '\\n  interests: ' + JSON.stringify(context.interests) + '\\n  now: ' + context.timeOfDay + ', ' + context.date + '\\n\\nCONNECTIONS (each tagged with suggestionType — produce exactly one matching suggestion per connectionId):\\n' + JSON.stringify(connectionsForPrompt, null, 2) + '\\n\\nNEARBY SPOTS (use exact names for detailed.place):\\n' + JSON.stringify(placesForPrompt, null, 2) + feedbackBlock + '\\n\\nGenerate the suggestions JSON now.';",
  '',
  '  let genResp;',
  '  try {',
  '    genResp = await groq.call(this, [',
  '      { role: \'system\', content: GENERATION_SYSTEM },',
  '      { role: \'user\', content: genUser },',
  '    ], 0.85, 1024);',
  '  } catch (e) {',
  "    feedback = 'Generator call failed: ' + (e.message || String(e));",
  '    continue;',
  '  }',
  '',
  "  const genContent = genResp.choices?.[0]?.message?.content || '{}';",
  '  let parsed;',
  '  try { parsed = JSON.parse(genContent); } catch { parsed = {}; }',
  '  const suggestions = Array.isArray(parsed.suggestions) ? parsed.suggestions : [];',
  '  lastSuggestions = suggestions;',
  '',
  '  if (suggestions.length === 0) {',
  "    feedback = 'Generator returned no suggestions';",
  '    continue;',
  '  }',
  '',
  '  // === DETERMINISTIC CHECKS ===',
  '  const detFeedback = [];',
  '  const knownSpots = new Set(placesForPrompt.map(p => p.name));',
  '  const seenPlaces = new Set();',
  '  const idCount = new Map();',
  '',
  '  for (const item of suggestions) {',
  "    if (!item || typeof item !== 'object') { detFeedback.push('NON-OBJECT: ' + JSON.stringify(item)); continue; }",
  '    const { type, connectionId, connectionName, text } = item;',
  '',
  "    if (type !== 'detailed' && type !== 'one_liner') { detFeedback.push('invalid type \"' + type + '\"'); continue; }",
  "    if (!connectionId || !expectedById.has(connectionId)) { detFeedback.push('unknown connectionId \"' + connectionId + '\" (valid: ' + Array.from(expectedById.keys()).join(', ') + ')'); continue; }",
  '',
  '    idCount.set(connectionId, (idCount.get(connectionId) || 0) + 1);',
  '    const expected = expectedById.get(connectionId);',
  "    if (type !== expected.type) detFeedback.push('connectionId ' + connectionId + ': type must be \"' + expected.type + '\" (got \"' + type + '\")');",
  "    if (connectionName !== expected.firstName) detFeedback.push('connectionId ' + connectionId + ': connectionName must be \"' + expected.firstName + '\" (got \"' + connectionName + '\")');",
  '',
  "    if (typeof text !== 'string' || !text.trim()) { detFeedback.push('connectionId ' + connectionId + ': empty text'); continue; }",
  "    if (!text.includes(connectionName)) detFeedback.push('connectionId ' + connectionId + ' (' + connectionName + '): text must mention name. Got: \"' + text + '\"');",
  "    if (text.length < 40 || text.length > 110) detFeedback.push('connectionId ' + connectionId + ': text length ' + text.length + ' outside 40-110 chars. Got: \"' + text + '\"');",
  '    for (const re of BANNED_PATTERNS) {',
  '      if (re.test(text)) {',
  "        detFeedback.push('connectionId ' + connectionId + ': BANNED phrase matching ' + re.source + ' in: \"' + text + '\"');",
  '        break;',
  '      }',
  '    }',
  "    if (type === 'detailed') {",
  '      const { title, place, time } = item;',
  "      if (typeof title !== 'string' || !title.trim()) detFeedback.push('connectionId ' + connectionId + ': missing title');",
  '      else {',
  "        const tw = title.trim().split(/\\s+/);",
  "        if (tw.length < 3 || tw.length > 6) detFeedback.push('connectionId ' + connectionId + ': title word count ' + tw.length + ' outside 3-6. Got: \"' + title + '\"');",
  '      }',
  "      if (typeof time !== 'string' || !time.trim()) detFeedback.push('connectionId ' + connectionId + ': missing time');",
  "      if (typeof place !== 'string' || !knownSpots.has(place)) {",
  "        detFeedback.push('connectionId ' + connectionId + ': place \"' + place + '\" not in nearby spots. Valid: ' + Array.from(knownSpots).join(' | '));",
  '      } else {',
  "        if (seenPlaces.has(place)) detFeedback.push('connectionId ' + connectionId + ': place \"' + place + '\" already used by another detailed suggestion — pick a different spot');",
  '        seenPlaces.add(place);',
  '      }',
  "      if (text.includes('?')) detFeedback.push('connectionId ' + connectionId + ': detailed text must not end with a question (it\\'s a plan, not a hook). Got: \"' + text + '\"');",
  '    }',
  "    if (type === 'one_liner') {",
  "      if (!text.trim().endsWith('?')) detFeedback.push('connectionId ' + connectionId + ': one_liner text must end with a question mark. Got: \"' + text + '\"');",
  '    }',
  '  }',
  '',
  '  // Exactly one per expected connectionId',
  '  for (const [id, meta] of expectedById.entries()) {',
  '    const n = idCount.get(id) || 0;',
  "    if (n === 0) detFeedback.push('MISSING: connectionId ' + id + ' (' + meta.firstName + ', ' + meta.type + ')');",
  "    else if (n > 1) detFeedback.push('DUPLICATE: connectionId ' + id + ' appears ' + n + ' times — exactly one allowed');",
  '  }',
  '',
  '  // Opening-word variety',
  "  const openings = suggestions.map(s => (s && typeof s.text === 'string') ? (s.text.trim().split(/[\\s,!?\\.]/)[0] || '').toLowerCase() : '').filter(Boolean);",
  '  const openingCount = new Map();',
  '  openings.forEach(o => openingCount.set(o, (openingCount.get(o) || 0) + 1));',
  '  for (const [o, n] of openingCount.entries()) {',
  "    if (n >= 3) detFeedback.push('OPENING VARIETY: \"' + o + '\" starts ' + n + ' texts — vary the opening word across suggestions');",
  '  }',
  '',
  '  if (detFeedback.length > 0) {',
  "    feedback = 'DETERMINISTIC CHECKS FAILED:\\n  - ' + detFeedback.join('\\n  - ');",
  '    continue;',
  '  }',
  '',
  '  // === LLM SUPERVISOR — subjective quality ===',
  "  const supUser = 'Suggestions to review (JSON below):\\n' + JSON.stringify(suggestions, null, 2) + '\\n\\nDeterministic checks (identity, types, fields, length, banned phrases, opening variety) already passed. Judge ONLY the 5 subjective rules. Respond with JSON in the format specified.';",
  '  let supResp;',
  '  try {',
  '    supResp = await groq.call(this, [',
  '      { role: \'system\', content: SUPERVISOR_SYSTEM },',
  '      { role: \'user\', content: supUser },',
  '    ], 0.1, 600);',
  '  } catch (e) {',
  "    feedback = 'Supervisor call failed: ' + (e.message || String(e));",
  '    continue;',
  '  }',
  '',
  "  const supContent = supResp.choices?.[0]?.message?.content || '{}';",
  '  let verdict;',
  "  try { verdict = JSON.parse(supContent); } catch { verdict = { verdict: 'REJECTED', feedback: 'Supervisor returned invalid JSON' }; }",
  '',
  "  if (typeof verdict.verdict === 'string' && verdict.verdict.toUpperCase() === 'APPROVED') {",
  '    approved = true;',
  '    break;',
  '  }',
  "  feedback = verdict.feedback || 'No specific supervisor feedback';",
  '}',
  '',
  '// Fallback: even if not approved, return the best-effort attempt so the UI has something to show.',
  '// Client can read approved=false and decide to hide/dim the cards.',
  'return [{ json: { suggestions: lastSuggestions, attempts: completedAttempts, approved, lastFeedback: feedback } }];',
].join('\n');

